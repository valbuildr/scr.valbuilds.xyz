    <p>
      When Roblox faced backlash from banning Youtuber Schlep, who's known for
      catching predators on the platform, they doubled down on their stance to
      remove vigilantes from the platform. <a
        href="#ref-2"
        id="subref-2"
        class="hover:underline text-blue-400 align-super text-sm">[2]</a
      >
    </p>
    <p>
      For the majority of my life, I've been playing Roblox. I'm deeply saddened
      with how things are going. I've made the decision to hault all of my
      Roblox-related projects, including this website, until things change.
      Minor updates, like fixing typos and security fixes will still occur.
    </p>
    <p>
      Louisiana's Attorney General has filed a lawsuit against Roblox for
      creating "an online environment where child sex predators thrive, unite,
      hunt and victimize kids".<a
        href="#ref-3"
        id="subref-3"
        class="hover:underline text-blue-400 align-super text-sm">[3]</a
      >
