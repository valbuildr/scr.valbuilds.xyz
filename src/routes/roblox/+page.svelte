      (Though Florida's AG has issued subpoenas.<a
        href="#ref-4"
        id="subref-4"
        class="hover:underline text-blue-400 align-super text-sm">[4]</a
      >)
    </p>
    <p>
      So, what can you do? I'm not one to say what to and what not to do, but
      you could start with cancelling your Roblox Premium subscription, if you
      have one. I've decided to boycott Roblox entirely.
